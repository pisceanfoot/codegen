namespace ChinaBest.BaseApplication.CGCoreLib
{
    public class CodeInfo
    {
        private string template;
        private string fileName;
        private string code;

        public string Template
        {
            get { return template; }
            set { template = value; }
        }

        public string FileName
        {
            get { return fileName; }
            set { fileName = value; }
        }

        public string Code
        {
            get { return code; }
            set { code = value; }
        }
    }
}